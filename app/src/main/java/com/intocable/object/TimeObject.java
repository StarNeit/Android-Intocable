package com.intocable.object;

import java.util.Date;

public class TimeObject {

	private Date date = null;

	public TimeObject() {
		super();
	}

	public TimeObject(Date date) {
		super();
		this.date = date;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

}
